// what is client side scripting
// Client-side scripting refers to scripts that run within your web browser. JavaScript is the most common language used for client-side scripting.
// JavaScript is a client-side scripting language, which means the source code is processed by the client's web browser rather than on the web server.
// This means JavaScript functions can run after a webpage has loaded without communicating with the server.


// what is server side scripting
// Server-side scripting is a technique used in web development which involves employing scripts on a web server which produce a response customized for each user's (client's) request to the website.
// Server-side scripting is often used to provide a customized interface for the user. The script can vary depending on the data provided by the user or the user's browser.


